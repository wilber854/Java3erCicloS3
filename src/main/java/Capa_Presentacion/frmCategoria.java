    public void listar_categorias() {
        n_categ.listarCategoria(tblCategorias);
    }

    public void agregar_categoria() {
        if (txtnom.getText().isEmpty() || txtDescrip.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Faltan agregar Datos");
        } else {
            Categoria categ = new Categoria();
            categ.setNombre(txtnom.getText());
            categ.setDescripcion(txtDescrip.getText());
            String mensaje = n_categ.agregarCategoria(categ);
            JOptionPane.showMessageDialog(null, mensaje);
            listar_categorias();
        }
    }

    public void actualizar_categoria() {
        Categoria categ = new Categoria();
        categ.setIdCategoria(Integer.parseInt(txtCodigo.getText()));
        categ.setNombre(txtnom.getText());
        categ.setDescripcion(txtDescrip.getText());
        String mensaje = n_categ.actualizarCategoria(categ);
        JOptionPane.showMessageDialog(null, mensaje);
        listar_categorias();

    }
    
    private void eliminar_categoria(){
        int cod=Integer.parseInt(txtCodigo.getText());
        String mensaje=n_categ.eliminarCategoria(cod);
        JOptionPane.showMessageDialog(null, mensaje);
        listar_categorias();
    }

    void clic_registro(){
        int fila_seleccionada=tblCategorias.getSelectedRow();
        txtCodigo.setText(tblCategorias.getValueAt(fila_seleccionada,0).toString());
        txtnom.setText(tblCategorias.getValueAt(fila_seleccionada,1).toString());
        txtDescrip.setText(tblCategorias.getValueAt(fila_seleccionada,2).toString());

    }
    
